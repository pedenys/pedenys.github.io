function getRandomNumber(min, max) {
  min = Math.ceil(min);
  max = Math.floor(max);
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

export function getRandomQuote() {
  const totalQuotes = quotes.length;
  const randomNumber = getRandomNumber(0, totalQuotes);
  const randomQuote = quotes[randomNumber];
  return randomQuote || quotes[0];
}
function populateQuote() {
  const randomQuote = getRandomQuote();
  const quoteContainer = document.querySelector("#quote");
  const blockQuote = quoteContainer?.querySelector("blockquote");
  const figCaption = quoteContainer?.querySelector("figcaption");

  if (quoteContainer && blockQuote && figCaption && randomQuote) {
    quoteContainer.style.display = "block";
    blockQuote.innerText = randomQuote.content;
    figCaption.innerText = randomQuote.author;
  }
}

populateQuote();
